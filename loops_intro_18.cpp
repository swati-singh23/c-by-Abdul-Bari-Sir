#include <iostream>
using namespace std;

int main()
{
    int a=0;
        
    while(a<10)
    {
        cout<<a<<"Hello\n";
        a++;
    }
    
    int b=0;
        
    do
    {
        cout<<b<<"Hello\n";
        b++;
    }
    while(b<10);
        
    for(int i=0;i<10;i++)
    {
        cout<<i<<"Hello\n";
    }
        
    return 0;
    
}
    
    